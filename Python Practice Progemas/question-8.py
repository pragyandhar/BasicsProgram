# Question-8: WAP to find the area and circumference of the circle, when the radius is entered by the user

# INPUT AREA
a = float(input("Enter the radius of the circle: "))
# LOGIC AREA
circ = 2*3.14*a
area = 3.14*a**2
# DISPLAYING AREA
print("The circumference of the circle with radius", a, "is", circ)
print("The area of the circle with radius", a, "is", area)
