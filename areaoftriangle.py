# Question-3: Write a program that displays the area of triangle with height and base as user input.

# INPUT SECTION
a = int(input("Enter the base of the triangle: "))
b = int(input("Enter the height of the triangle: "))

# LOGIC SECTION
c = 0.5 * a * b

# DISPLAY SECTION
print("The area of triangle with base", a, "and height", b, "is:", c)
